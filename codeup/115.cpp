#include<iostream>
using namespace std;

int main(){
	cin.tie(NULL); 
	ios_base::sync_with_stdio(false);
	
	int a,b;
	cin >> a >> b;
	
	cout << a << "+" << b << "=" <<a+b<<"\n";
	cout << a << "-" << b << "=" <<a-b<<"\n";
	cout << a << "*" << b << "=" <<a*b<<"\n";
	cout << a << "/" << b << "=" <<a/b<<"\n";
	return 0;
}
